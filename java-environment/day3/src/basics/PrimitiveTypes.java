/*
 * The package declaration should be the first line
 * of every Java class file.  
 * 
 * The package declaration informed the program
 * of where the information for the classes exist 
 * in relation to one another.  
 */
package basics;

public class PrimitiveTypes {
	public static void main(String[] args) {
		byte b = 100;
		short s = 110;
		int i = 120;
		long l = 130L;
		float f = 3.14F;
		double d = 3.14;
		char c = 'C';
		boolean boolT = true;
		boolean boolF = false;
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
	}

}
